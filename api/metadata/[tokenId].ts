export default async function handler(req: NextApiRequest, res: NextApiResponse) {
    const { tokenId } = req.query;

    try {
        // 连接到Reddio Devnet
        const provider = new ethers.providers.JsonRpcProvider("https://reddio-dev.reddio.com");

        // 初始化合约
        const nftContract = new ethers.Contract(NFT_CONTRACT_ADDRESS, NFT_CONTRACT_ABI, provider);

        // 获取NFT数据
        const record = await nftContract.getClimbingRecord(tokenId);

        // 生成NFT元数据
        const metadata = {
            name: `ClimbingUp Reddio #${tokenId}`,
            description: `A record of ${record.isAscent ? 'ascending' : 'descending'} ${record.meters} meters on the Reddio Devnet. Final height: ${record.height} meters.`,
            image: `https://climbing-reddio-api.vercel.app/api/image/${tokenId}`,
            attributes: [
                {
                    trait_type: "Height",
                    value: record.height.toString()
                },
                {
                    trait_type: "Action",
                    value: record.isAscent ? "Ascent" : "Descent"
                },
                {
                    trait_type: "Meters",
                    value: record.meters.toString()
                },
                {
                    trait_type: "Timestamp",
                    value: new Date(record.timestamp.toNumber() * 1000).toISOString()
                }
            ]
        };

        res.status(200).json(metadata);
    } catch (error) {
        console.error("Error generating metadata:", error);
        res.status(500).json({ error: "Failed to generate metadata" });
    }
} 